package com.tekion.cricketmatch.match.matchstats;

public class InningStats {
  private int inningScore;
  private int inningWickets;

  public int getInningScore() {
    return inningScore;
  }

  public void setInningScore(int inningScore) {
    this.inningScore = inningScore;
  }

  public int getInningWickets() {
    return inningWickets;
  }

  public void setInningWickets(int inningWickets) {
    this.inningWickets = inningWickets;
  }
}
